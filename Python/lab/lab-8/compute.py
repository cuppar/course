"""
这是一个计算模块，可进行+,-,*,/,**运算
"""


def add(a, b):
    """
    对输入的两个参数求和
    a: 被加数
    b: 加数
    return: 和
    """
    return a+b


def sub(a, b):
    """
    对输入的两个参数求差
    a: 被减数
    b: 减数
    return: 差
    """
    return a-b


def mul(a, b):
    """
    对输入的两个参数求积
    a: 被乘数
    b: 乘数
    return: 积
    """
    return a*b


def div(a, b):
    """
    对输入的两个参数求商
    a: 被除数
    b: 除数
    return: 商
    """
    return a/b


def pow(a, b):
    """
    对输入的第一个参数求第二个参数的幂
    a: 基数
    b: 指数
    return: 幂
    """
    return a**b
